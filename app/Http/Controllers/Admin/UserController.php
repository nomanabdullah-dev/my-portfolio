    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'name'      =>'required|string',
            'email'     =>'required|email',
            'phone'     =>'required|string',
            'profession'=>'required|string',
            'location'  =>'required|string',
            'map_link'  =>'required|string'
        ]);

        $user = User::find($id);
        $user->name     = $request->name;
        $user->email    = $request->email;
        $user->phone    = $request->phone;
        $user->profession = $request->profession;
        $user->location = $request->location;
        $user->map_link = $request->map_link;

        if($request->password){
            $haspass = Hash::make($request->password);
            $user->password = $haspass;
        }

        if($request->file('image')){
            $img_file = $request->file('image');
            $img_file->storeAs('/public/img/','image.'.$img_file->getClientOriginalExtension());
            $user->image = 'storage/img/image.'.$img_file->getClientOriginalExtension();
        }

        $user->save();

        return redirect()->route('admin.user.index')->withSuccess('Your data has been updated successfully!');
    }

